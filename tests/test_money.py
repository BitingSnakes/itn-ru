import pytest

from rus.wfst import normalize


@pytest.mark.parametrize(
    "spoken,expected",
    [
        ("один рубль", "₽1"),
        ("один рубль одна копейка", "₽1.01"),
        ("один рубль двадцать одна копейка", "₽1.21"),
        ("двадцать один рубль двадцать одна копейка", "₽21.21"),
        ("сто одиннадцать долларов двадцать один цент", "$111.21"),
        ("сто одиннадцать долларов и двадцать один цент", "$111.21"),
        ("двадцать один цент", "$0.21"),
        ("двадцать одна копейка", "₽0.21"),
        ("семь копеек", "₽0.07"),
        ("пять целых одна десятая рубля", "₽5.1"),
        ("пять целых одна десятая миллиона рублей", "₽5.1 миллиона"),
        ("тридцать тысяч сто один рубль", "₽30101"),
    ],
)
def test_money(spoken, expected):
    assert normalize(spoken) == expected


@pytest.mark.parametrize(
    "spoken,expected",
    [
        ("сто евро", "€100"),
        ("два евро пятьдесят евроцентов", "€2.50"),
        ("пятьдесят евроцентов", "€0.50"),
        ("пять фунтов стерлингов", "£5"),
        ("десять фунтов", "£10"),
        ("два фунта двадцать пенсов", "£2.20"),
        ("двадцать пенсов", "£0.20"),
    ],
)
def test_euro_pound(spoken, expected):
    assert normalize(spoken) == expected


