import pytest

from rus.wfst import normalize


@pytest.mark.parametrize(
    "spoken,expected",
    [
        ("ноль целых одна десятая процента", "0.1 %"),
        ("минус пять целых одна десятая процента", "-5.1 %"),
        ("пять процентов", "5 %"),
        ("сто пять процентов", "105 %"),
        ("сто процентов", "100 %"),
    ],
)
def test_percent(spoken, expected):
    assert normalize(spoken) == expected


@pytest.mark.parametrize(
    "spoken,expected",
    [
        ("сто двадцать километров", "120 км"),
        ("двадцать два метра", "22 м"),
        ("пять сантиметров", "5 см"),
        ("три миллиметра", "3 мм"),
        ("минус пять целых три десятых килограмма", "-5.3 кг"),
        ("пятьдесят граммов", "50 г"),
        ("три тонны", "3 т"),
        ("два литра", "2 л"),
        ("сто миллилитров", "100 мл"),
        ("сорок гектаров", "40 га"),
        ("десять секунд", "10 с"),
        ("пять процентов", "5 %"),
    ],
)
def test_units(spoken, expected):
    assert normalize(spoken) == expected


