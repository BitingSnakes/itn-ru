import pytest

from rus.wfst import normalize


@pytest.mark.parametrize(
    "spoken,expected",
    [
        ("седьмой час двадцать пять минут", "07:25"),
        ("седьмой час пять минут", "07:05"),
        ("двенадцатый час пять минут", "12:05"),
        ("половина десятого", "09:30"),
        ("половина первого", "00:30"),
        ("половина третьего", "02:30"),
        ("половина двадцать четвертого", "23:30"),
        ("четверть одиннадцатого", "10:15"),
        ("без четверти одиннадцатая", "10:45"),
        ("пять минут двенадцатого", "11:05"),
        ("сорок семь минут двадцать второго", "21:47"),
        ("двенадцатый ноль ноль", "12:00"),
        ("двенадцатый час ноль ноль", "12:00"),
        ("одиннадцатый ноль шесть", "11:06"),
        ("шестой сорок три", "06:43"),
    ],
)
def test_time(spoken, expected):
    assert normalize(spoken) == expected


@pytest.mark.parametrize(
    "spoken,invalid_output",
    [
        ("двадцать четвертый час пять минут", "24:05"),
        ("двадцать пятый час пять минут", "25:05"),
        ("двенадцатый час шестьдесят минут", "12:60"),
    ],
)
def test_invalid_clock_values_are_not_emitted(spoken, invalid_output):
    assert normalize(spoken) != invalid_output


@pytest.mark.parametrize(
    "spoken,expected",
    [
        ("третий час дня", "15:00"),
        ("третий вечера", "15:00"),
        ("двенадцатый ночи", "00:00"),
        ("третий час пять минут по московскому времени", "03:05 Europe/Moscow"),
        ("половина десятого по киевскому времени", "09:30 Europe/Kyiv"),
        ("двенадцатый ноль ноль по всемирному координированному времени", "12:00 UTC"),
    ],
)
def test_time_periods_and_zones(spoken, expected):
    assert normalize(spoken) == expected
